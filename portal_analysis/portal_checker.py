import re


# ──────────────────────────────────────────────────────────────────────────────
# БЛОК 1: Значения, которые считаются «не заполнено» (IRRELEVANT)
# ──────────────────────────────────────────────────────────────────────────────

IRRELEVANT_VALUES = {
    '', 'none', 'null', 'не указано', 'не применимо',
    'не заполнено', 'отсутствует',  # TODO: уточнить — «отсутствует» в преф. режиме = ЗАПОЛНЕНО
}


def _strip_html(s: str) -> str:
    """Удаляет HTML-теги."""
    return re.sub(r'<[^>]+>', '', s).strip()


def _is_empty(value) -> bool:
    """Возвращает True, если значение считается незаполненным."""
    if value is None:
        return True
    v = _strip_html(str(value)).strip().lower()
    return v in IRRELEVANT_VALUES


# ──────────────────────────────────────────────────────────────────────────────
