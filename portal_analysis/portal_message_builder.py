# Подсказка по умолчанию, если поле не описано выше
_DEFAULT_HINT = 'Заполните поле на портале invest.gov.ru.'


# ──────────────────────────────────────────────────────────────────────────────
# БЛОК 2: Вспомогательные функции
# ──────────────────────────────────────────────────────────────────────────────

def _get_field_value(row: dict, field: str) -> str:
    """Возвращает очищенное значение поля из строки БД."""
    normalized = {k.strip().lower(): v for k, v in row.items()}
    val = normalized.get(field.strip().lower(), '')
    return _strip_html(str(val or '')).strip()


def _get_contact(row: dict) -> str:
    """Извлекает ФИО контактного лица."""
    return _get_field_value(row, 'Контактное лицо') or ''


def _get_site_name(row: dict) -> str:
    """Извлекает название площадки."""
    return _get_field_value(row, 'Название площадки') or 'Без названия'


def _get_site_id(row: dict) -> str:
    """Извлекает ID площадки."""
    # Пробуем несколько вариантов названия колонки ID
    for key in ('id', 'ид', 'id площадки', 'идентификатор'):
        normalized = {k.strip().lower(): v for k, v in row.items()}
        val = normalized.get(key, '')
        if val:
            return str(val).strip()
    return ''


def _hint_for(field: str) -> str:
    """Возвращает подсказку для поля."""
    return FIELD_HINTS.get(field.strip().lower(), _DEFAULT_HINT)


def _format_site_block(row: dict) -> str:
    """
    Формирует блок сообщения для одной площадки:
    - Название + ID + score
    - Пронумерованный список missing с подсказками
    """
    result = calc_portal_score(row)
    score   = result['score']
    missing = result['missing']
    total   = result['total']

    name    = _get_site_name(row)
    site_id = _get_site_id(row)

    id_part = f' (ID: {site_id})' if site_id else ''
    delta   = 100 - score
    per_field = round(delta / len(missing)) if missing else 0

    lines = []
    lines.append(f'📍 «{name}»{id_part} — заполняемость {score}%')

    if not missing:
        lines.append('✅ Все поля заполнены!')
    else:
        lines.append(
            f'Для повышения показателя просим заполнить {len(missing)} '
            f'поля(-ей) (каждое даёт ~+{per_field}%):'
        )
        for i, field in enumerate(missing, 1):
            hint = _hint_for(field)
            lines.append(f'{i}. {field} — {hint}')

    return '\n'.join(lines)


# ──────────────────────────────────────────────────────────────────────────────
# БЛОК 3: Основная функция — группировка по контактному лицу
# ──────────────────────────────────────────────────────────────────────────────

def build_messages(rows: list) -> list:
    """
    Группирует площадки по контактному лицу и формирует
    одно сообщение для мессенджера на каждого контакта.

    Args:
        rows: список словарей из БД (одна строка = одна площадка)

    Returns:
        Список словарей:
        [
          {
            'contact': 'Иванов И.И.',   # ФИО контактного лица
            'sites':   [название, ...], # список площадок в группе
            'text':    '...',           # готовый текст сообщения
          },
          ...
          # последний элемент — блок «Без контакта» (если есть)
        ]
    """
    # Шаг 1: группируем строки по контактному лицу
    groups: dict[str, list] = {}
    no_contact: list = []

    for row in rows:
        contact = _get_contact(row)
        if not contact:
            no_contact.append(row)
        else:
            groups.setdefault(contact, []).append(row)

    messages = []

    # Шаг 2: строим сообщение для каждого контакта
    for contact, contact_rows in sorted(groups.items()):
        site_blocks = []
        site_names  = []

        for row in contact_rows:
            site_names.append(_get_site_name(row))
            site_blocks.append(_format_site_block(row))

        divider = '─' * 40

        header = (
            f'Добрый день, {contact}!\n\n'
            f'Направляю информацию о заполняемости площадок на портале '
            f'invest.gov.ru, по которым Вы указаны как контактное лицо.'
        )

        body = ('\n\n' + divider + '\n').join(site_blocks)

        footer = (
            '\nПосле внесения изменений просим сообщить — '
            'проверим обновлённый процент.'
        )

        text = f'{header}\n\n{divider}\n{body}\n{footer}'

        messages.append({
            'contact': contact,
            'sites':   site_names,
            'text':    text,
        })

    # Шаг 3: блок «Без контакта» (если есть)
    if no_contact:
        site_blocks = []
        site_names  = []

        for row in no_contact:
            site_names.append(_get_site_name(row))
            site_blocks.append(_format_site_block(row))

        divider = '─' * 40

        header = (
            '⚠️ ПЛОЩАДКИ БЕЗ КОНТАКТНОГО ЛИЦА\n\n'
            'По следующим площадкам не указано контактное лицо. '
            'Рекомендуется заполнить поле «Контактное лицо» в КИТЕЖ.'
        )

        body = ('\n\n' + divider + '\n').join(site_blocks)

        text = f'{header}\n\n{divider}\n{body}'

        messages.append({
            'contact': '__no_contact__',
            'sites':   site_names,
            'text':    text,
        })

    return messages


